package com.umbrella.bureaux.myweather;

public class CitySearchItemClass {
    private String cnty;
    private String location;
    private String tz;
    private String admin_area;
    private String lon;
    private String lat;
    private String parent_city;
    private String cid;

    public String getCnty() { return cnty; }
    public void setCnty(String value) { this.cnty = value; }

    public String getLocation() { return location; }
    public void setLocation(String value) { this.location = value; }

    public String getTz() { return tz; }
    public void setTz(String value) { this.tz = value; }

    public String getAdminArea() { return admin_area; }
    public void setAdminArea(String value) { this.admin_area = value; }

    public String getLon() { return lon; }
    public void setLon(String value) { this.lon = value; }

    public String getLat() { return lat; }
    public void setLat(String value) { this.lat = value; }

    public String getParentCity() { return parent_city; }
    public void setParentCity(String value) { this.parent_city = value; }

    public String getCid() { return cid; }
    public void setCid(String value) { this.cid = value; }

}
