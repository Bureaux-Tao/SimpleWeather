package com.umbrella.bureaux.myweather;
import java.util.*;
public class HourlyBean {
    private String cloud;
    private String cond_code;
    private String cond_txt;
    private String dew;
    private String hum;
    private String pop;
    private String pres;
    private String time;
    private String tmp;
    private String wind_deg;
    private String wind_dir;
    private String wind_sc;
    private String wind_spd;

    public String getCloud() { return cloud; }
    public void setCloud(String value) { this.cloud = value; }

    public String getCondCode() { return cond_code; }
    public void setCondCode(String value) { this.cond_code = value; }

    public String getCondTxt() { return cond_txt; }
    public void setCondTxt(String value) { this.cond_txt = value; }

    public String getDew() { return dew; }
    public void setDew(String value) { this.dew = value; }

    public String getHum() { return hum; }
    public void setHum(String value) { this.hum = value; }

    public String getPop() { return pop; }
    public void setPop(String value) { this.pop = value; }

    public String getPres() { return pres; }
    public void setPres(String value) { this.pres = value; }

    public String getTime() { return time; }
    public void setTime(String value) { this.time = value; }

    public String getTmp() { return tmp; }
    public void setTmp(String value) { this.tmp = value; }

    public String getWindDeg() { return wind_deg; }
    public void setWindDeg(String value) { this.wind_deg = value; }

    public String getWindDir() { return wind_dir; }
    public void setWindDir(String value) { this.wind_dir = value; }

    public String getWindSc() { return wind_sc; }
    public void setWindSc(String value) { this.wind_sc = value; }

    public String getWindSpd() { return wind_spd; }
    public void setWindSpd(String value) { this.wind_spd = value; }
}
